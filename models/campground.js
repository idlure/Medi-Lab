var mongoose=require("mongoose");

var campgroundschema=new mongoose.Schema({
    name: String,
    email: String,
    phone: Number,
    day: String,
    slot: String,
    description: String,
    author:{
        id:Number,
        username: String,
        gmailid: String,
        image: String
    },
    txn_id: String,
    btxn_id: String,
    doctor_id: String,
});

module.exports=mongoose.model("campground",campgroundschema);  